
public class BOOKRVALUES {

	private String r,bn,bi,sd;

	public BOOKRVALUES(String r, String bn, String bi, String sd) {
		super();
		this.r = r;
		this.bn = bn;
		this.bi = bi;
		this.sd = sd;
	}

	public String getR() {
		return r;
	}

	public void setR(String r) {
		this.r = r;
	}

	public String getBn() {
		return bn;
	}

	public void setBn(String bn) {
		this.bn = bn;
	}

	public String getBi() {
		return bi;
	}

	public void setBi(String bi) {
		this.bi = bi;
	}

	public String getSd() {
		return sd;
	}

	public void setSd(String sd) {
		this.sd = sd;
	}
	
	
}
