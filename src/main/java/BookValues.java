
public class BookValues {
	
	private String r,bn,bi,id,rd;

	
	public BookValues(String r, String bn, String bi, String id, String rd) {
		super();
		this.r = r;
		this.bn = bn;
		this.bi = bi;
		this.id = id;
		this.rd = rd;
	}

	public String getR() {
		return r;
	}

	public void setR(String r) {
		this.r = r;
	}

	public String getBn() {
		return bn;
	}

	public void setBn(String bn) {
		this.bn = bn;
	}

	public String getBi() {
		return bi;
	}

	public void setBi(String bi) {
		this.bi = bi;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRd() {
		return rd;
	}

	public void setRd(String rd) {
		this.rd = rd;
	}

	
}
